	OR REPLACE VIEW DW_DWH_V.V_DWH_D_ORG_COMPANY_LU AS

SELECT COMPANY_KEY
	,COMPANY_ID
	,COMPANY_DESC
	,RCD_INS_TS
	,RCD_UPD_TS
	,RCD_CLOSE_FLG
	,RCD_CLOSE_DT
FROM DW_DWH.DWH_D_ORG_COMPANY_LU;


